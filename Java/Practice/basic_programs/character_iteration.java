package Practice.basic_programs;
import java.util.*;

public class character_iteration {
    public static void main(String[] args) {
        for(char ch = '1' ; ch<='9' ; ch++){
            System.out.print(ch+" ");
        }
    }



    // Time Complexity : O(n)
    // Space Complexity : O(1)
}
